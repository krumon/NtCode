namespace Nt.Core.Data
{
    /// <summary>
    /// The type of swing. The swing can be formed by high prices or low prices
    /// </summary>
    public enum SwingType
    {

        /// <summary>
        /// Swing formed by high prices.
        /// </summary>
        High,

        /// <summary>
        /// Swing formed by low prices.
        /// </summary>
        Low,

        /// <summary>
        /// Any swing.
        /// </summary>
        Indifferent,

    }
}
