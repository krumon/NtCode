namespace Nt.Core.Data
{
    /// <summary>
    /// Type of length of the arrow head.
    /// </summary>
    public enum ArrowHeadType
    {
        /// <summary>
        /// The arrow head length is equal that arrow base.
        /// </summary>
        Normal,

        /// <summary>
        /// The arrow head length is more large than arrow base.
        /// </summary>
        Large,

        /// <summary>
        /// The arrow head length is less large than arrow base.
        /// </summary>
        Short,

        /// <summary>
        /// The arrow head length is 0.
        /// </summary>
        Flat,

        /// <summary>
        /// The arrow head length is so much large than arrow base.
        /// </summary>
        VeryLarge,

        /// <summary>
        /// The arrow head length is so much large than arrow base.
        /// </summary>
        VeryShort,

    }
}
